import UIKit

extension UIFont {
    // MARK: SpoqaHanSansNeo Font
    class func spoqaLight(size: CGFloat) -> UIFont {
        return UIFont(name: "SpoqaHanSansNeo-Light", size: size)!
    }
    
    class func spoqaRegular(size: CGFloat) -> UIFont {
        return UIFont(name: "SpoqaHanSansNeo-Regular", size: size)!
    }
    
    class func spoqaMedium(size: CGFloat) -> UIFont {
        return UIFont(name: "SpoqaHanSansNeo-Medium", size: size)!
    }
    
    class func spoqaBold(size: CGFloat) -> UIFont {
        return UIFont(name: "SpoqaHanSansNeo-Bold", size: size)!
    }
}



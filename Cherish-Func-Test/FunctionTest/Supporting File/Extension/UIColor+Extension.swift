import Foundation
import UIKit

extension UIColor {
    // 제플린에서 컬러셋 넘어오면 추가로 변경할게요!@!~
    
    @nonobjc class var customgreen: UIColor {
        return UIColor(red: 0/255.0, green: 171/255.0, blue: 162/255.0, alpha: 1.0)
    }
    
    @nonobjc class var customred: UIColor {
        return UIColor(red: 247/255.0, green: 89/255.0, blue: 108/255.0, alpha: 1.0)
    }
    
}
